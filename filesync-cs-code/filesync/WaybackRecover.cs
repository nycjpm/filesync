        // Final integrity verification of the January 2026 NAS migration from ds1019 (oldnas)
        // to ds1525 (newnas). Run once after all sync operations completed to confirm newnas was a perfect copy.
        //
        // Hash files were generated by running md5sum on both NAS devices via SSH:
        // - oldnas hashed its own volumes locally and wrote results to \\newnas\scratch\hash\oldnas-{share}.hash
        // - newnas hashed its own volumes locally and wrote results to \\newnas\scratch\hash\newnas-{share}.hash
        // This approach avoided hashing across the network in both directions simultaneously, which had previously
        // crashed the workstation ethernet driver. Each NAS read its own local disks; only the small hash result
        // files crossed the network.
        //
        // The hash files contain md5sum output format: "hash  path". Paths are normalized by replacing the volume
        // prefix (/volume1/ on oldnas, /volume2/ on newnas) with "/" so paths are comparable across machines.
        // Synology @eaDir entries are excluded - these are DSM-generated thumbnail and metadata directories that
        // exist independently on each NAS and are not part of the migrated data.
        //
        // Result: 801,662 files verified across data, code, media, photo, download, video-source, and video-edit.
        // Zero missing, zero mismatches. One extra file on newnas (a Storage Analyzer symlink that was resolved
        // to a real file by SMB during the copy, and was excluded from the oldnas hash because find -type f skips
        // symlinks). Migration declared complete and verified.
        //
        // DO NOT USE THIS CODE. It is preserved for reference only. The hash files it reads no longer exist and
        // the migration scenario it verified is complete.

